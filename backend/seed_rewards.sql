INSERT INTO reward_inventory (reward_id, total_qty, reserved_qty, sold_qty)
SELECT id, 
  CASE 
    WHEN point_cost <= 150 THEN 100
    WHEN point_cost <= 300 THEN 50
    ELSE 20
  END,
  0, 0
FROM rewards
WHERE tenant_id = '00000000-0000-0000-0000-000000000001'
  AND id NOT IN (SELECT reward_id FROM reward_inventory);

SELECT r.name, r.point_cost, r.cost_currency, r.type, r.delivery_type, ri.total_qty
FROM rewards r
LEFT JOIN reward_inventory ri ON ri.reward_id = r.id
WHERE r.tenant_id = '00000000-0000-0000-0000-000000000001'
ORDER BY r.point_cost;

